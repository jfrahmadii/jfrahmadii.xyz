/**
 * build-site-data.mjs — derive the website's chart data from the same star-schema
 * fact tables that feed the Power BI / Fabric model, so the site can never drift
 * from the pipeline. Run from the repo root:
 *
 *   node tools/build-site-data.mjs
 *
 * Reads  : Fact_Generation_ON_annual.csv, Fact_Generation_ON_monthly.csv
 * Writes : assets/js/ontario-data.js  (window.ONTARIO_DATA = {...})
 *
 * "Emissions-free %" follows the dashboard's definition: all generation except
 * Natural Gas (GAS) and the small mixed Other (OTH) category.
 */
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const FUELS = ["NUC", "HYD", "WND", "SOL", "BIO", "GAS", "OTH"];
const EMITTING = new Set(["GAS", "OTH"]);
const round1 = (n) => Math.round(n * 10) / 10;

function parseCsv(text) {
  const [header, ...lines] = text.trim().split(/\r?\n/);
  const cols = header.split(",");
  return lines.map((line) => {
    const cells = line.split(",");
    return Object.fromEntries(cols.map((c, i) => [c, cells[i]]));
  });
}

/** Pivot fact rows -> { keys:[...sorted], series:{FUEL:[gwh per key]}, total, cleanPct } */
function pivot(rows, keyOf) {
  const byKey = new Map(); // key -> { FUEL: gwh }
  for (const r of rows) {
    const key = keyOf(r);
    if (!byKey.has(key)) byKey.set(key, {});
    byKey.get(key)[r.FuelKey] = Number(r.Generation_GWh);
  }
  const keys = [...byKey.keys()].sort();
  const series = Object.fromEntries(FUELS.map((f) => [f, []]));
  const total = [];
  const cleanPct = [];
  for (const key of keys) {
    const row = byKey.get(key);
    let tot = 0;
    let emit = 0;
    for (const f of FUELS) {
      const v = round1(row[f] ?? 0);
      series[f].push(v);
      tot += v;
      if (EMITTING.has(f)) emit += v;
    }
    total.push(round1(tot));
    cleanPct.push(tot ? round1(((tot - emit) / tot) * 100) : 0);
  }
  return { keys, series, total, cleanPct };
}

const annualRows = parseCsv(readFileSync(resolve(ROOT, "Fact_Generation_ON_annual.csv"), "utf8"));
const monthlyRows = parseCsv(readFileSync(resolve(ROOT, "Fact_Generation_ON_monthly.csv"), "utf8"));

const annual = pivot(annualRows, (r) => r.Year);
const monthly = pivot(monthlyRows, (r) => `${r.Year}-${String(r.Month).padStart(2, "0")}`);

const data = {
  meta: {
    source: "IESO Generator Output Capability (transmission-connected generators)",
    generated: new Date().toISOString().slice(0, 10),
    fuelOrder: FUELS,
  },
  annual: { years: annual.keys.map(Number), ...pick(annual) },
  monthly: { labels: monthly.keys, ...pick(monthly) },
};

function pick({ series, total, cleanPct }) {
  return { series, total, cleanPct };
}

const out = `/* AUTO-GENERATED by tools/build-site-data.mjs — do not edit by hand.
   Source: IESO Generator Output Capability fact tables (star schema). */
window.ONTARIO_DATA = ${JSON.stringify(data, null, 2)};
`;

mkdirSync(resolve(ROOT, "assets/js"), { recursive: true });
writeFileSync(resolve(ROOT, "assets/js/ontario-data.js"), out);

// Console sanity check against known published figures.
const i2025 = data.annual.years.indexOf(2025);
console.log("Wrote assets/js/ontario-data.js");
console.log(`  annual years : ${data.annual.years.join(", ")}`);
console.log(`  2025 total   : ${(data.annual.total[i2025] / 1000).toFixed(1)} TWh (expect ~162.4)`);
console.log(`  2025 clean % : ${data.annual.cleanPct[i2025]}% (expect ~80.1)`);
console.log(`  monthly pts  : ${data.monthly.labels.length} (${data.monthly.labels[0]} -> ${data.monthly.labels.at(-1)})`);
